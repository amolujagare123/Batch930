package ClassExample;

public class castingExample {

    public static void main(String[] args) {

          int a = 10;
          int b = 3 ;

          double c = (double) a/b;

          // if you wan to assign one type of value (e.g. a/b) to another (double c) type
          // then you have to do casting
         // to do casting you put the type for e.g. double- into  () - round bracket
        // in front of the value to be casted

        System.out.println("division:"+c);


    }
}
